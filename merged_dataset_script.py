import pandas as pd, numpy as np

social = pd.read_excel("customer_social_profiles.xlsx")
trans  = pd.read_excel("customer_transactions.xlsx")

print("social:", social.shape, "| unique customers:", social.customer_id_new.nunique())
print("trans :", trans.shape,  "| unique customers:", trans.customer_id_legacy.nunique())

# --- the key fix: A178 -> 178 ---
social["customer_id"] = social["customer_id_new"].str.extract(r"(\d+)").astype(int)
trans = trans.rename(columns={"customer_id_legacy": "customer_id"})

overlap = set(social.customer_id) & set(trans.customer_id)
print("customers in both:", len(overlap))

def mode_or_nan(x):
    m = x.mode()
    return m.iloc[0] if len(m) else np.nan

soc_agg = social.groupby("customer_id").agg(
    engagement_mean   = ("engagement_score", "mean"),
    engagement_max    = ("engagement_score", "max"),
    engagement_std    = ("engagement_score", "std"),
    interest_mean     = ("purchase_interest_score", "mean"),
    interest_max      = ("purchase_interest_score", "max"),
    n_social_records  = ("customer_id", "size"),
    n_platforms       = ("social_media_platform", "nunique"),
    main_platform     = ("social_media_platform", mode_or_nan),
    main_sentiment    = ("review_sentiment", mode_or_nan),
).reset_index()

# ordinal sentiment -> numeric average
sent_map = {"Negative": -1, "Neutral": 0, "Positive": 1}
sent = (social.assign(sv=social.review_sentiment.map(sent_map))
              .groupby("customer_id")["sv"].mean()
              .rename("sentiment_mean").reset_index())

soc_agg = soc_agg.merge(sent, on="customer_id")
soc_agg["engagement_std"] = soc_agg["engagement_std"].fillna(0)   # single-record customers
print(soc_agg.shape)   # (84, 11)
soc_agg.head()

merged = trans.merge(soc_agg, on="customer_id", how="inner")
print("merged:", merged.shape)                       # (117, 16)
print("customers:", merged.customer_id.nunique())    # 61
print(merged.product_category.value_counts())
merged.head()

df = merged.copy()

# --- date features ---
df["purchase_date"] = pd.to_datetime(df["purchase_date"])
df["purchase_month"]   = df["purchase_date"].dt.month
df["purchase_dow"]     = df["purchase_date"].dt.dayofweek
df["is_weekend"]       = (df["purchase_dow"] >= 5).astype(int)

# --- transaction behaviour per customer ---
cust = df.groupby("customer_id").agg(
    cust_n_transactions = ("transaction_id", "size"),
    cust_avg_spend      = ("purchase_amount", "mean"),
    cust_total_spend    = ("purchase_amount", "sum"),
).reset_index()
df = df.merge(cust, on="customer_id", how="left")
df["spend_vs_avg"] = df["purchase_amount"] / df["cust_avg_spend"]

# --- cross-source interaction features (the interesting ones) ---
df["engagement_x_interest"] = df["engagement_mean"] * df["interest_mean"]
df["spend_per_engagement"]  = df["purchase_amount"] / (df["engagement_mean"] + 1)
df["interest_x_sentiment"]  = df["interest_mean"] * df["sentiment_mean"]

# --- missing values ---
df["customer_rating"] = df["customer_rating"].fillna(df["customer_rating"].median())

# --- encode categoricals ---
df = pd.get_dummies(df, columns=["main_platform", "main_sentiment"], drop_first=False)

df.to_csv("merged_dataset.csv", index=False)
